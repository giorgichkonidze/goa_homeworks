let age = prompt("შეიყვანე თქვენი ასაკი: ");

if (age >= 80) {
    console.log("დაბერდი ძმაო");
} else if (age >=12) {
    console.log("შენ ხარ მოზარდი");
} else if (age >= 18) {
    console.log("შენ ხარ სრულწლოვანი");
} else if (age >= 5) {
    console.log("შენ ხარ ბავშვი");
} else {
    console.log("შენ ხარ ძალიან პატარა");
}